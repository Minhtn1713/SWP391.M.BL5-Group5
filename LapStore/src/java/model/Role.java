package model;

/**
 *
 * @author hieu
 */
public class Role {
    private String id;
    private String RoleName;

    public Role() {
    }

    public Role(String id, String RoleName) {
        this.id = id;
        this.RoleName = RoleName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRoleName() {
        return RoleName;
    }

    public void setRoleName(String RoleName) {
        this.RoleName = RoleName;
    }

}
